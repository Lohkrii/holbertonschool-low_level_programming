#include <stdio.h>

/**
 * main - Prints the values of all the types on the computer.
 *
 * Return: Must be 0 to work
*/
int main(void)
{
printf("Size of a char: %ld byte(s)\n", sizeof(char));
printf("Size of an int: %ld byte(s)\n", sizeof(int));
printf("Size of a long int: %ld byte(s)\n", sizeof(int));
printf("Size of a long long: %ld byte(s)\n", sizeof(int));
printf("Size of a float: %ld byte(s)\n", sizeof(int));
return (0);
}
